package ddr.example.com.newretailandroidclient.entity.other;

public class HuoProduct {
    private String id;
    private String name;
    private String imageLan;
    private String imageWan;
    private String description;
    private String price;
    private String startdata;
    private String lifedata;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImageLan() {
        return imageLan;
    }

    public void setImageLan(String imageLan) {
        this.imageLan = imageLan;
    }

    public String getImageWan() {
        return imageWan;
    }

    public void setImageWan(String imageWan) {
        this.imageWan = imageWan;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getStartdata() {
        return startdata;
    }

    public void setStartdata(String startdata) {
        this.startdata = startdata;
    }

    public String getLifedata() {
        return lifedata;
    }

    public void setLifedata(String lifedata) {
        this.lifedata = lifedata;
    }
}
