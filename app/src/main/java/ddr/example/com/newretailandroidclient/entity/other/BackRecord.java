package ddr.example.com.newretailandroidclient.entity.other;

public class BackRecord {
    private String id;
    private String time;
    private String pay_f;
    private String back_f;
    private String pay_num;
    private String back_result;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getPay_f() {
        return pay_f;
    }

    public void setPay_f(String pay_f) {
        this.pay_f = pay_f;
    }

    public String getBack_f() {
        return back_f;
    }

    public void setBack_f(String back_f) {
        this.back_f = back_f;
    }

    public String getPay_num() {
        return pay_num;
    }

    public void setPay_num(String pay_num) {
        this.pay_num = pay_num;
    }

    public String getBack_result() {
        return back_result;
    }

    public void setBack_result(String back_result) {
        this.back_result = back_result;
    }
}
